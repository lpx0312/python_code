'''
3、元素分类
    有如下值li= [11,22,33,44,55,66,77,88,99,90]，将所有大于 66 的值保存至字典的第一个key中，将小于 66 的值保存至第二个key的值中。
即： {'k1': 大于66的所有值列表, 'k2': 小于66的所有值列表}
'''

li = [11,22,33,44,55,66,77,88,99,90]
k1 = []
k2 = []

dict = {}
for i in li:
    if i == 66:
        continue
    elif i > 66 :
        k1.append(i)
    else:
        k2.append(i)


dict["k1"] = k1
dict["k2"] = k2
print(dict)

